from django.shortcuts import render
from . import forms

def index(request):
    form = forms.UploadForm(request.GET or None)
    if form.is_valid():
        message = 'データ検証に成功しました'
    else:
        message = 'データ検証に失敗しました'
    d = {
        'form': form,
        'message': message,
    }
    return render(request, 'app/index.html', d)
